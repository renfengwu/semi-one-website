const compactRdsOn = (
  values: { voltage: '10V' | '4.5V' | '2.5V' | '1.8V'; value: number | null }[]
) =>
  Object.fromEntries(
    values
      .filter(
        (item): item is { voltage: '10V' | '4.5V' | '2.5V' | '1.8V'; value: number } =>
          item.value !== null
      )
      .map((item) => [item.voltage, item.value])
  ) as Product['rdsOn'];

